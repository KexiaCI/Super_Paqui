        for k, v in dom.items():
            f.write(f"    '{k}': {v},\n")
        f.write("}\n\n")
        
        f.write("DESTINOS_OCURRE = {\n")
        for k, v in ocu.items():
            f.write(f"    '{k}': {v},\n")
        f.write("}\n")

